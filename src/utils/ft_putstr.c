#include <unistd.h>

#include "utils.h"

void	ft_putstr(const char *str)
{
	write(1, str, ft_strlen(str));
}

bool	ft_isalnum(const char c)
{
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z') || (c >= '!' && c <= '/')
		|| (c >= ':' && c <= '@') || (c >= '[' && c <= '`')
		|| (c >= '{' && c <= '~'))
		return (true);
	return (false);
}
